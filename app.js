let currentModule = 0;
let examQuestions = [];

const progress = JSON.parse(localStorage.getItem("fertTrainerProgress") || '{"completed":[],"practiceCorrect":0,"practiceTotal":0,"examScores":[]}');

function saveProgress(){ localStorage.setItem("fertTrainerProgress", JSON.stringify(progress)); updateProgress(); }

function updateProgress(){
  const pct = Math.round((progress.completed.length / modules.length) * 100);
  document.getElementById("overallProgress").textContent = pct + "%";
}

function renderModules(){
  const list = document.getElementById("moduleList");
  list.innerHTML = "<h3>Modules</h3>";
  modules.forEach((m, i) => {
    const btn = document.createElement("button");
    btn.className = "module-button" + (progress.completed.includes(i) ? " complete" : "");
    btn.textContent = m.title.replace(" — ", "\n");
    btn.onclick = () => { currentModule = i; renderModule(); renderModules(); };
    if(i === currentModule) btn.classList.add("active");
    list.appendChild(btn);
  });
}

function renderModule(){
  const m = modules[currentModule];
  const html = `
    <p class="eyebrow">Study Mode</p>
    <h2>${m.title}</h2>
    <p><strong>${m.summary}</strong></p>
    ${m.content.map(([h,b]) => `<div class="lesson-block"><h3>${h}</h3><p>${b}</p></div>`).join("")}
    <div class="memory-card"><strong>Memory Card:</strong><br>${m.memory}</div>
  `;
  document.getElementById("moduleContent").innerHTML = html;
}

function setupTabs(){
  document.querySelectorAll(".tab").forEach(tab => {
    tab.onclick = () => {
      document.querySelectorAll(".tab,.view").forEach(el => el.classList.remove("active"));
      tab.classList.add("active");
      document.getElementById(tab.dataset.view).classList.add("active");
      if(tab.dataset.view === "dashboard") renderDashboard();
    };
  });
}

function setupStudyButtons(){
  document.getElementById("prevModule").onclick = () => { currentModule = Math.max(0, currentModule - 1); renderModule(); renderModules(); };
  document.getElementById("nextModule").onclick = () => { currentModule = Math.min(modules.length - 1, currentModule + 1); renderModule(); renderModules(); };
  document.getElementById("markComplete").onclick = () => {
    if(!progress.completed.includes(currentModule)) progress.completed.push(currentModule);
    saveProgress(); renderModules();
  };
}

function setupPractice(){
  const sel = document.getElementById("practiceModule");
  sel.innerHTML = '<option value="all">All Modules</option>' + modules.map((m,i)=>`<option value="${i}">${m.title}</option>`).join("");
  document.getElementById("newPractice").onclick = renderPracticeQuestion;
  renderPracticeQuestion();
}

function renderPracticeQuestion(){
  const selected = document.getElementById("practiceModule").value;
  const pool = selected === "all" ? questions : questions.filter(q => q.module == selected);
  const q = pool[Math.floor(Math.random() * pool.length)];
  const box = document.getElementById("practiceQuestion");
  box.innerHTML = `<h3>${q.q}</h3>` + q.options.map((o,i)=>`
    <label class="option"><input type="radio" name="practice" value="${i}"> ${String.fromCharCode(65+i)}. ${o}</label>
  `).join("") + `<button id="checkPractice">Check Answer</button><div id="practiceFeedback"></div>`;
  document.getElementById("checkPractice").onclick = () => {
    const chosen = document.querySelector('input[name="practice"]:checked');
    if(!chosen){ document.getElementById("practiceFeedback").innerHTML = '<div class="feedback bad-feedback">Choose an answer first.</div>'; return; }
    const correct = Number(chosen.value) === q.answer;
    progress.practiceTotal++;
    if(correct) progress.practiceCorrect++;
    saveProgress();
    document.getElementById("practiceFeedback").innerHTML = `<div class="feedback ${correct ? "" : "bad-feedback"}"><strong>${correct ? "Correct" : "Incorrect"}.</strong> ${q.explain}</div>`;
  };
}

function startExam(){
  examQuestions = [...questions].sort(()=>Math.random()-.5).slice(0,20);
  document.getElementById("gradeExam").disabled = false;
  document.getElementById("examBox").innerHTML = examQuestions.map((q,idx)=>`
    <div class="question-box">
      <h3>${idx+1}. ${q.q}</h3>
      ${q.options.map((o,i)=>`<label class="option"><input type="radio" name="exam${idx}" value="${i}"> ${String.fromCharCode(65+i)}. ${o}</label>`).join("")}
    </div>
  `).join("");
}

function gradeExam(){
  let correct = 0;
  let review = "";
  examQuestions.forEach((q,idx)=>{
    const chosen = document.querySelector(`input[name="exam${idx}"]:checked`);
    const ok = chosen && Number(chosen.value) === q.answer;
    if(ok) correct++;
    review += `<div class="feedback ${ok ? "" : "bad-feedback"}"><strong>${idx+1}. ${ok ? "Correct" : "Review"}:</strong> ${q.explain}</div>`;
  });
  const score = Math.round((correct/examQuestions.length)*100);
  progress.examScores.push(score);
  saveProgress();
  document.getElementById("examBox").innerHTML = `<h3>Score: ${score}%</h3><p>${score >= 85 ? "Pass target met." : "Keep studying. Target is 85%+."}</p>${review}`;
  document.getElementById("gradeExam").disabled = true;
}

function setupExam(){
  document.getElementById("startExam").onclick = startExam;
  document.getElementById("gradeExam").onclick = gradeExam;
}

